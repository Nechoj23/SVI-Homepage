            <p align="left">TSV B&ouml;nnigheim 1 - SVI 1</p>
            
            <p>
            	Die Illinger Erste hat bei ihrem Auswärtsspiel in Bönnigheim einer alten Fußballweisheit einmal mehr neues 
				Gewicht verliehen: "Ein Spiel dauert 90 Minuten".
				Angefangen hat aus Sicht der Blauen alles recht gut, hatte man doch gleich nach dem Spielbeginn die große 
				Chance in Führung zu gehen, welche allerdings nicht genutzt wurde. Danach spielte man umständlich und 
				überhastet, wodurch man einen spielerisch unterlegenen Gegner zunehmend aubaute. Nach 15 Minuten und 
				einem unnötigen Ballverlust konnte der Gastgeber die Illinger Fahrlässigkeit zum ersten mal ausnutzen 
				und vollendete aus Mittelstürmerposition zum 1:0. Weitere 10 Minuten später eine ähnliche Situation - 2:0. 
				Es dauerte bis zur 40. Spielminute, ehe der SVI wieder ein Zeichen setzte. Oliver Rapp leitete den 
				Anschlusstreffer zum 2:1 selbst ein und konnte nach einem herrlichen Doppelpass mit Pucci kurz vor der Pause 
				verkürzen. <br>
				Die zweite Hälfte begann mit Illinger Powerplay, doch Gruica und Pucci vergaben allerbeste Torchancen. 
				Bönnigheim spielte einen Konter, Illingens Fritsch holte den gegnerischen Angreifer von den Beinen und 
				der gute Schiedsrichter entschied sofort auf Strafstoß, welcher souverän zum 3:1 verwandelt wurde. 
				In der 65. Minuten schien es dann knüppeldick für die Eichwaldelf zu kommen. Nach einem Drehschuss 
				aus knapp 15 Metern konnte der Bönnigheimer Mittelstürmer seine Mannen mit seinem 4. Tor mit 4:1 in Front 
				schießen. Illingen brauchte ein bisschen, doch kam dann fulminant zurück. In der 80. Minute war es Kammerlohr, 
				der nach einem schönen Gruica-Solo nur noch einschieben musste - 4:2. Nach 85 Minuten dann ein Gala-Auftritt 
				von Illingens Defensiv-Mann Demirdag, der von der Mittellinie zum Sololauf ansetzte und dann herrlich für 
				Gruica auflegte - 4:3. Ebendieser Demirdag war es dann auch, der in der 90. Spielminute den kurz zuvor 
				eingewechselten Loewe mustergültig bediente, welcher den hochverdienten 4:4 Endstand markieren konnte.<br>
				Mit diesem Kraftakt konnte man Bönnigheim in der Tabelle auf Distanz halten und belegt weiter den 7. Platz.
            </p>
