            <p>TSV Aurich 1 - SVI 2</p>
            <p>
                Die zweite Mannschaft des SVI musste heute sein vorletztes Auswärtsspiel beim Tabellenletzten 
				TSV Aurich bestreiten. Die Mannschaft wollte dieses Spiel unbedingt gewinnen und zudem etwas ganz 
				Besonderes erreichen: --> Die "Null" sollte hinten stehen bleiben! <br>
				Den besseren Start in die Partie erwischte Aurich, doch einzelne Vorstöße und Torschüsse wurden 
				durch gute Devensivarbeit abgewehrt. Im Laufe der Zeit kam dann der SVI besser in die Partie und 
				erspielte sich einige Torchancen. Zudem wurde ein Foulspiel an Davor nicht mit Elfmeter gewertet. 
				Diesen gab es aber kurz vor der Pause nach einem Handspiel. Den fälligen Elfer verwandelte Mustafa 
				sicher zum 1-0. So ging es dann auch in die Pause. <br>
				Nach dem Seitenwechsel ließen die Illinger dann Ball und Gegner laufen; Aurich schwächte sich selbst 
				dann durch 2 Platzverweise. Während der 2.Halbzeit erhöhten dann 2xDavor, Lennox, Richie (als Stürmer!) 
				und Captain Matze Spengler auf 6-0! Das war auch das Endergebnis, welches auch in der Höhe verdient war. <br>
				...3 Punkte und endlich mal steht auch die "Null" 
			</p>
