          <td id="contentFont" colspan="3" style="text-align:left; "><p><br />
            Vergangene Woche veranstaltete die Fu&szlig;ballabteilung ihr  mittlerweile 8. Jugendcamp im Eichwaldpark. Nachdem am vorletzten Tag noch ein  Kind zu uns stie&szlig;,&nbsp; durften wir dieses  Mal exakt 69 Kinder bzw. Jugendliche bei uns begr&uuml;&szlig;en. Obwohl alle Beteiligten  sp&auml;testens am Freitagabend &bdquo;in den Seilen&ldquo; hingen, hat es auch dieses Mal  wieder einen Riesenspa&szlig; gemacht, diese Veranstaltung bei uns durchzuf&uuml;hren!  Allein die Freude der Kinder in den drei Tagen war es einfach wert. Und ihr,  liebe Kinder, Ihr wart einfach klasse!<br />
            Wir bedanken uns hiermit bei der Sparkasse Pforzheim-Calw,  bei den Firmen Fontanis und Bonbon-Jung sowie bei Michas Freizeitshop f&uuml;r die  Unterst&uuml;tzung. Dank allen Helfern, ohne die so etwas gar nicht m&ouml;glich w&auml;re.  Die Trainer haben ein absolut kurzweiliges Programm geboten, wobei erw&auml;hnt  werden muss, dass da mancher Urlaubstag geopfert wurde. Auf unsere jugendlichen  Assistenztrainer, die einen Super-Job gemacht haben,&nbsp; k&ouml;nnen wir hoffentlich auch in der Zukunft  bauen (!?). Die K&uuml;che unter Federf&uuml;hrung von Hubert L. Scheuermann hat in  bew&auml;hrter Manier p&uuml;nktlich zu den Essenszeiten geliefert, so dass die Kalorien  anschlie&szlig;end wieder auf dem Sportplatz abgebaut werden konnten.<br />
            Ein besonderes Dankesch&ouml;n an die Platzwarte Andy und Wolfe  f&uuml;r das herrliche Gel&auml;nde und an Herrn Efthimios Bakas (&bdquo;Thimo&ldquo;), der uns im  Clubhaus &bdquo;wurschteln&ldquo; lie&szlig; und uns als Kr&ouml;nung auch noch eine Geldspende hat zukommen lassen.<br />
            Irgendwie war es wieder toll!</p><p align="center">
