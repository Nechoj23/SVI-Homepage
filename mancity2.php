          Blaue,<br>
          abseits der Schaubühne Premier League freuen sich die Fans in England auf das FA-Cupfinal am Samstag, 17. Mai. Dann wird um 15.00 Uhr im Wembleystadion das Endspiel angepfiffen und wenn alles normal verläuft, werden diesmal zwei Nationalhymen gespielt. Zunächst, natürlich die englische Hymne und dann folgendes Stück: Hen wlad fy nhadau, übersetzen läßt sich dieser Zungenbrecher ungefähr mit "Land of my fathers". Dieses Stück ist die Hymne von Wales und Cardiff City ist neben dem FC Portsmouth der zweite Finalist. Cardiff liegt bekanntlich in Wales und wenn es böld läuft, geht der Pott sozusagen außer Landes. Das wäre für das Mutterland des Fussballs eine weitere Schmach und so ruhen alle Hoffnungen auf dem aktuellen Tabellenachten der Premiership, dem FC Portsmouth. Die Hafenstädter sind auch Favorit, denn mit einem Gewinn ist die Teilnahme am UEFA-Cup sicher. Cardiff spielt stellvertretend für alle Waliser und konnte schon einmal den FA-Cup gewinnen, und zwar 1927. Überhaupt waren die Zanziger die besten Jahre, zwei Vizemeisterschaften in der ehemaligen Southern League bezeugen die damalige Stärke. In der heutigen zweiten Liga belegt Cardiff City einen bescheidenen 12. Platz und mit Robbie Fowler hat man auch schon den bekanntesten Namen parat. Der nunmehr 35-jähriger Stürmer hatte seine beste Zeit bei Liverpool, war ebenfalls auch bei ManCITY unter Vertrag und beschließt in Wales nun seine wechselhafte Karriere. Vielleicht läßts Robbie zum Ausklang nochmals knallen ! Zu seinen Exclubs gehört wie schon erwähnt ManCITY und damit sind wir wieder mal beim Thema. In der nächsten Woche planen die Fans einen Marsch zum Stadion und wollen der Vereinsleitung eine Liste mit über 20.000 Unterschriften überreichen, alle Unterzeichner wollen Sven-Goran Erikksson als Trainer behalten. Der thailändische Besitzer Thaksin Shinawatra rudert derweil zurück, indem er die Feststellung relativiert, daß Erikksson zum Saisonende entlassen wird. Er habe lediglich angedeutet, dass man überaus kritisch die nächste Saison angehen muß. Entweder kann der Maqnn ganz auf die Schnelle keine Trainer auftreiben, oder die ganze Angelegenheit wird zu heiß. Zumal vermutlich die Saison mit einem Überraschungsknall endet.  
Sollte ManCITY das letzte Match beim FC Middlesborough ohne größere Entgleisungen über die Bühne bringen, startet man im nächsten Jahr im UEAFA-Cup. Am Freitag bewilligte die UEFA einen weiteren Starterplatz für England aufgrund der Fairplaywertung. Hier ist England an erster Stelle.  
ManCITY steht in der englischen Fairplaytabelle auf Rang fünf. Da Arsenal, Tottenham, ManU und Liverpool bereits im internationalen Geschäft sind, dürfte ManCITY sich ebenfalls auf Europa freuen. Bereits in der Saison
2002/03 rutschte man unter Trainer Kevin Keagan über diesen Umweg in den UEFA-Cup. Immerhin, ManCITY wäre auf einen Schlag viel attraktiver und für manchen Neuzugang interessanter.
