		  		<p align="left">Seit vielen Jahren genie&szlig;t die Fu&szlig;ballabteilung des SV Illingen   aufgrund ihrer sehr guten und positiven Jugendarbeit gro&szlig;es Ansehen in   Illingen und der Region. H&auml;ufig wechseln Kinder aus der n&auml;heren Umgebung   zu &bdquo;unserem SVI&ldquo;.<br />
