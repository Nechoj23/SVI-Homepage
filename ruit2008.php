      <td id="contentFont" colspan="2" style="text-align:center; "> <p align="left">Der 
          Gro&szlig;teil der Illinger Aktiven verweilte &uuml;bers Wochenende 
          in der Stuttgarter Sportschule, um sich intensiv auf die am kommenden 
          Mittwoch beginnende Runde vorzubereiten. Am fr&uuml;hen Freitagnachmittag 
          waren alle angereist und hatten ihre Zimmer bezogen, so dass mit der 
          sehnlichst erwarteten ersten Trainingseinheit begonnen werden konnte. 
          Leider musste man schon nach wenigen Minuten die ersten Ausf&auml;lle 
          beklagen und auch das Wetter erinnerte eher an Fritz Walter. Doch dessen 
          unbeirrt wurde das vorgenommene Programm durchgezogen, weshalb die Mannschaft 
          abends ersch&ouml;pft, aber vorbildlich die Betten noch deutlich vor 
          Mitternacht fand. Somit konnte die Nacht ausschlie&szlig;lich zu Regenerationszwecken 
          genutzt werden. Nachdem der Samstag mit einem fr&uuml;hmorgendlichen 
          Joggen um 7.00Uhr (!) begonnen hatte, wurde vormittags parallel zu den 
          Einheiten auf dem Platz am f&uuml;r den Nachmittag geplanten Highlight 
          gearbeitet. Nach z&auml;hen und sich schwierig gestaltenden Verhandlungen 
          konnte zum Mittagessen schlie&szlig;lich verk&uuml;ndet werden, dass 
          um 17:00Uhr ein Spiel gegen die Nationalmannschaft aus Sambia stattfinden 
          wird, die ebenso in der Sportschule zu Gast war. Die ausgemachte 60-min&uuml;tige 
          Spielzeit wurde zu einer Lehrstunde f&uuml;r den SVI, wie man das von 
          Illingen ebenso praktizierte Spielsystem in Perfektion aus&uuml;bt. 
          Trotz einer &#8222;knappen&#8220; 7:0-Niederlage waren alle Beteiligten 
          gl&uuml;cklich, die M&ouml;glichkeit gehabt zu haben, ihr erstes &#8222;L&auml;nderspiel&#8220; 
          zu bestreiten. V&ouml;llig ausgelaugt und mit neuen Erkenntnissen fand 
          man sich nach der Sportschau zum abendlichen Kartenspiel in der Gastst&auml;tte 
          ein. Dort war jedoch schon zu diesem Zeitpunkt eine afrikanische Party 
          in vollem Gange, unter deren G&auml;sten sich auch der VFB-Spieler Roberto 
          Hilbert mit Familie befand. Sonntags wurde nach einem, von der Nationalmannschaft 
          bekannten, leichten &#8222;Anschwitzen&#8220; das abschlie&szlig;ende 
          Mittagessen eingenommen und anschlie&szlig;end machte sich der SV Illingen 
          auf nach Poppenweiler zum anstehenden Pokalspiel. Das somit erste Pflichtspiel 
          konnte trotz schwerer Beine souver&auml;n mit 5:1 gewonnen werden. Bis 
          auf die verletzungsbedingten Ausf&auml;lle kann auf ein gelungenes und 
          ereignisreiches Trainingslager zur&uuml;ckgeblickt werden. </p>
