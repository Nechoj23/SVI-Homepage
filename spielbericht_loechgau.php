          <td id="contentFont" colspan="2" style="text-align:center; "><p align="left">1.Mannschaft: 
          L&ouml;chgau 2 - SVI 1<br>
          <br>
          Chance nicht genutzt&#8230;&#8230;.<br>
          Die zahlreich nach L&ouml;chgau mitgereisten Zuschauer, sahen eine vom 
          Trainer gut eingestellte Illinger Mannschaft, die auch gleich unterbeweis 
          Stellen wollte das die Serie von neun ungeschlagenen Spielen fortgesetzt 
          wird.<br>
          Doch wie so oft wurde die sich bietende Torchancen nicht genutzt, so 
          auch als Matthias Pucci in der 15 min. unsere Mannschaft in F&uuml;hrung 
          bringen musste, der aber freistehend und alleine aufs L&ouml;chgauer 
          Tor zugehen nicht abschlie&szlig;en konnte. Es folgten weitere Chancen 
          in F&uuml;hrung zu gehen, Michael Schlosser in der 36 min. unser Au&szlig;enverteidiger 
          setzte sich nach feiner Einzelaktion auf der der rechten Seite durch, 
          doch dessen scharf nach innen gezogener Ball konnte nicht verwertet 
          werden.<br>
          Die Abwehr stand wie auch in den letzten Spielen sehr gut und Diszipliniert, 
          Organisiert von Bj&ouml;rn Kachel und Selim Demirdag, bis zur 41. Min 
          als ein L&ouml;chgauer Verteidiger nach einem Freisto&szlig; am 5 Meterraum 
          eink&ouml;pfen konnte.<br>
          So ging es auch in die Pause. In der 53 min. keimte nochmals Hoffnung 
          auf als Jonathan Kammerlohr &uuml;berlegt ins unter rechte Tor eck zum 
          1:1 Ausgleicht einschoss. <br>
          Nun dr&auml;ngt unsere Mannschaft, die Angriffsbem&uuml;hungen verpufften 
          aber immer wieder an der abgekl&auml;rten Spielweise der L&ouml;chgauer 
          Hintermannschaft, so geriet man in der 61 min. wiederum nach einer Standardsituation 
          in R&uuml;ckstand nach dem L&ouml;chgaus St&uuml;rmer Lanotti zum 2:1 
          einschoss. Unsere Mannschaft steckte nicht auf und versuchte bis zum 
          Schluss alles, wurde aber in der 90 min. nach einem Freisto&szlig; f&uuml;r 
          Illingen Ausgekontert. <br>
          L&ouml;chgaus Boriello lie&szlig; sich diese Konterchance nicht nehmen 
          und schob zum 3:1 Endstand ein. Im n&auml;chsten Heimspiel gegen H&auml;fnerhaslach 
          muss unsere Mannschaft erfolgreich sein um nicht weiter in der Tabelle 
          abzurutschen, vielleicht starten unsere Jungs wieder eine Serie und 
          best&auml;tigen dennoch den Aufw&auml;rtstrend.<br>
