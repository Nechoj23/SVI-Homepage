						<p>
							Der SVI spielte im ersten Saisonspiel von Beginn an druckvollen Offensivfu&szlig;ball und lie&szlig;
							die G&auml;ste aus Kirchheim kaum zur Entfaltung kommen. Immer wieder kam man &uuml;ber die Fl&uuml;gel
							brandgef&auml;hrlich vor gegnerische Tor. So auch nach 20 Min., als Pucci goldrichtig stand und nach 
							Vorlage von Johannes Kammerlohr zum verdienten 1:0 einschieben konnte. Der SVI setzte direkt nach 
							und kam nach erneut feiner Vorarbeit von Johannes Kammerlohr zum 2:0 durch Fl&uuml;gelflitzer 
							Kn&ouml;dler. Weitere gute Torm&ouml;glichkeiten wurden ausgelassen.<br></br>
							In der zweiten Halbzeit machte Illingen da weiter, wo man in den ersten 45 Minuten aufegh&ouml;rt 
							hatte. Man erspielte sich gr&ouml;&szlig;te Torchancen, konnte sie jedoch allesamt nicht verwerten.
							Kirchheim kam einmal vors Illinger Tor, bekam einen Freisto&szlig; aus gef&auml;hrlicher Position und 
							verwandelte eiskalt und wundersch&ouml;n zum 2:1 Anschlusstreffer. Illingen lie&szlig; sich nun 
							etwas aus der Ruhe bringen, wodurch das Spiel an Hektik und Dramatik zunahm. In der Nachspielzeit 
							bekam verlor der SVI das Leder im Spielaufbau, wodurch Kirchheim freie Bahn zum Tor hatte, jedoch 
							zun&auml;chst freistehend an den Pfosten ballerte, um dann per Nachschuss das 2:2 zu erzielen und 
							das Ergebnis zu egalisieren. Doch wer dachte, das Spiel sei nun zu Ende, der sah sich schwer get&auml;uscht. 
							Illingen versuche nochmal alles und bekam tats&auml;chlich nach einem fragw&uuml;rdigen Foul noch einen 
							Strafsto&szlig; zugesprochen. Doch Jonathan Kammerlohr scheiterte am gl&auml;nzend reagierenden G&auml;stekeeper.
							Illingen machte weiter, hatte in dieser Nachspielzeit noch hochkar&auml;tige M&ouml;glichkeiten, 
							um ein ganzes Spiel zu entscheiden. Brekalos Kopfball an den Pfosten war dann allerdings die letzte, und so 
							blieb es am Ende beim 2:2 und 2 klar verschenkten Punkten auf Illinger Seite.
