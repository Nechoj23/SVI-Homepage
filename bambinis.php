            <p>Mit dem im Januar 2008 gewonnenen Wanderpokal im Gep&auml;ck  machten wir<br />
              uns am Sonntag, 18.01.2009, auf den Weg zu unserem 1.  Hallenturnier in 2009 zum FV Kirchheim am Neckar.<br />
              Mit dem Wunsch eine anst&auml;ndige Vorrunde zu spielen, h&auml;tten  die Jungs uns<br />
              Trainer schon gl&uuml;cklich gemacht. Aber es sollte viel besser  werden.<br />
              Was diese Bambinis an den Tag legten, war f&uuml;r uns alle  SVI`ler (ob Eltern,<br />
              Freunde, Verwandte oder Trainer) unfassbar und zugleich ein  Traum.<br />
              Mit 5 Feldspielern plus Torwart wurde gespielt und unter den  Gegnern waren wirklich starke Mannschaften dabei.<br />
              Unsere Youngsters gingen mit vollem Eifer und Siegeswillen  in ihre Spiele und<br />
              gewannen ihre 3 Vorrundenpaarungen eindrucksvoll jeweils zu  &bdquo;Null&ldquo; (0).<br />
              Das wir ins Halbfinale kamen, war schon ein super Ding und  ein kleines Wunder.<br />
              Als dann noch dieses Halbfinale erstklassig und  Bundesligareif gewonnen<br />
              wurde, gab es kein Halten mehr f&uuml;r unsere wilden Stars &bdquo;wir  sind im Endspiel/<br />
              wir sind im Finale&ldquo;.<br />
              Sie wollten den Pokal wieder mit nach Illingen bringen.<br />
              Doch der Finalgegner VFL Gemmrigheim war durch das  Vorrundenspiel vor<br />
              uns gewarnt.<br />
              Das dieses Spiel, so eindrucksvoll und Klasse es auch war,  nicht gewonnen<br />
              wurde, brach allen unseren K&auml;mpfern das Herz.<br />
              Mit 2 : 1 besiegt zu werden, war zwar traurig, denn  k&auml;mpferische Leistung<br />
              zeigten unsere Jungs von der ersten Minute an, aber den 2.  Platz zu erreichen,<br />
              war vor Beginn des Turniers schon ein Traum.<br />
              Euch Superhelden m&ouml;chten wir ein gaaaaanz dickes Lob  aussprechen, denn f&uuml;r<br />
              uns Trainer ist ein kleines &bdquo;Winterm&auml;rchen&ldquo; wahr geworden.<br />
              Diese Leistung habt Ihr gebracht und Euch geh&ouml;rt unser  ganzer Stolz. Bitte<br />
              macht weiter so. Einfach traumhaft.</p>
