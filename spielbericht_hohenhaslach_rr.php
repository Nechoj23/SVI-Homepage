            <p align="left">SC Hohenhaslach 1 - SVI 1</p>
            
            <p>
                Der SVI konnte an seine starken Vorstellungen der letzten beiden Wochen nicht ankn&uuml;pfen und lie&szlig; beim noch gegen den Abstieg k&auml;mpfenden SC Hohenhaslach 2 Punkte. Das Spiel, welches aufgrund der noch nicht abgeschlossenen Sportplatzarbeiten beim Gastgeber auf dem Kunstrasenplatz in Freudental stattfand, war nicht wirklich etwas f&uuml;r Fu&szlig;ball&auml;stheten. Viele Ballverluste im Spielaufbau, Fouls und Nicklichkeiten h&uuml;ben wie dr&uuml;ben. Der SVI hatte wiedereinmal seine liebe Not mit dem Kunstrasen und kam nur sehr schwer ins Spiel. Dann jedoch - nach einem sch&ouml;nen Spielzug &uuml;ber die rechte Seite - war es Gruica, der das Leder halb mit dem Kopf halb mit der Schulter unhaltbar ins linke untere Eck "einnickte". Hohenhaslach hatte auf der anderen Seiite ebenfalls eine Riesenm&ouml;glichkeite, doch die SVI-Abwehr konnte auf der Linie retten. Illingens Hinkelmann musste bereits nach 20 Minuten verletzungsbedingt ausgewechselt werden. F&uuml;r ihn kam Michael Mahler auf die linke Abwehrseite und sorgte hier mit seinen Vorst&ouml;&szlig;en immer wieder f&uuml;r Unruhe in der Hintermannschaft der Gastgeber.<br>
Die zweite Halbzzeit war ein Spiegelbild der ersten, nur diesemal mit dem gl&uuml;cklicheren Ende f&uuml;r Hohenhaslach. Nach einem Abwurf von Illingens Fritsch auuf Schlosser verlor dieser auf der rechten Abwehrseite den Ball an den Gegner. Dieser schatete blitzschnell und spielte den Ball am zur&uuml;ckeilenden Fritsch vorbei ins Illinger Tor zum 1:1 Ausgleich. Das wars dann im Gro&szlig;en und Ganzen auch. Beide Teams hatten keine zwingenden M&ouml;glichkeiten mehr und so trennte man sich in einem schlechten Spiel leistungsgerecht mit 1:1 unentschieden.
