          SVI 1 - Sersheim 1<br>
          <br>
          Der SV Illingen war im Spiel gegen den VfR Sersheim vom Anpfiff weg das dominierende Team. Aggressives Vorchecking und ein gesundes Zweikampfverhalten ließen die Gäste zu keiner Zeit zur Entfaltung kommen. Illingen spielte sich ein Vielzahl an guten Torchancen heraus, die Anfangs jedoch fahrl&auml;ssig vergeben wurden. Nach 20 Minuten klappte es dann endlich, als Lukas Kn&ouml;dler einen Abpraller zum hochverdienten 1:0 in die Maschen schieben konnte. Der SVI machte weiter Druck und konnte durch Michael Schlosser einen schlimmen Abwehrpatzer der Gäste in der 40. Minute auf 2:0 erh&ouml;hen.

Als niemand mehr mit großer Gegenwehr rechnete sprang Hinkelmann praktisch mit dem Halbzeitpfiff das Leder an die Hand und der Unparteiische entschied sofort auf Strafstoß, welcher seitens der G&auml;ste souver&auml;n zum 2:1 Anschlusstreffer verwandelt werden konnte.

 
In der zweiten Halbzeit hatte Illingen zunächst großes Gl&uuml;ck, nicht den Ausgleich zu kassieren, doch Schlosser rettete in h&ouml;chster Not für seinen geschlagenen Keeper auf der Linie. Mit zunehmender Dauer nahm Illingen das Heft wieder in die Hand und machte so weiter, wie man die ersten 45 Minuten beendet hatte. In der 70. Minute konnte Gracher nach einem Freistoß auf 3:1 erh&ouml;hen, ehe Rapp in der 90. Minute zum hochverdienten 4:1 Endstand verwandelte. Letztendlich konnte Sersheim aufgrund der Vielzahl hochkar&auml;tiger Illinger Torm&ouml;glichkeiten sehr froh sein, nicht noch höher verloren zu haben. Der SVI hingegen hat sich nach den eher zurückhaltenden Spielen der jüngsten Vergangenheit eindrucksvoll zurückgemeldet.</p></td>
